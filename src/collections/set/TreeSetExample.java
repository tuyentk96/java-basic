package collections.set;

import java.util.TreeSet;

public class TreeSetExample {
    public static void main(String[] args) {
        TreeSet<String> set = new TreeSet<String>();
        set.add("Java");
        set.add("JavaScript");
        set.add("Python");
        set.add("C++");

        System.out.println("Show TreeSet: " + set);

    }
}
