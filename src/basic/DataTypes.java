package basic;

public class DataTypes {
    public static void main(String[] args) {
        String name = "Tuyen";
        int age = 18;
        float gpa = 3.0f;
        char gender = 'M';
        boolean married = false;
        System.out.println(
                "2. Data type example:\n"+
                        "Name: "+ name +"\n"+
                        "Age: " + age +"\n"+
                        "GPA: " + gpa +"\n"+
                        "Gender: " + gender +"\n"+
                        "Married: " + married);
    }
}
