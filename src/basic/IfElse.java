package basic;

public class IfElse {
    public static void main(String[] args) {

        // If example
        int x = 20;
        int y = 18;
        System.out.println("If example: ");
        if (x>y){
            System.out.println("x is greater than y");
        }

        // If else example
        int time = 16;
        System.out.println("If-else example: ");
        if(time < 18){
            System.out.println("Good Day!");
        }else {
            System.out.println("Good Evening!");
        }
    }
}
